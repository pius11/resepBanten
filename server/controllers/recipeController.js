require('../models/database');
const Category = require('../models/Category');
const Recipe = require('../models/Recipe');

/**
 * GET /
 * Homepage 
*/
exports.homepage = async(req, res) => {
  try {
    const limitNumber = 5;
    const categories = await Category.find({}).limit(limitNumber);
    const latest = await Recipe.find({}).sort({_id: -1}).limit(limitNumber);
    const nista = await Recipe.find({ 'category': 'nista' }).limit(limitNumber);
    const madya = await Recipe.find({ 'category': 'madya' }).limit(limitNumber);
    const utama = await Recipe.find({ 'category': 'utama' }).limit(limitNumber);

    const banten = { latest, nista, madya, utama };

    res.render('index', { title: 'Banten Bali - Home', categories, banten } );
  } catch (error) {
    res.status(500).send({message: error.message || "Error Occured" });
  }
}

